#include <iostream>
#include <cstring>
int main() { // read an integer from user input and pass it to a function to copy a string into a character array and print the character at the specified index
    int n = readInt();
    char *s = new char *n;
    for (int i = 0; i < n; i++) {
        s[i] = readChar();
    }
    printf(""%c"", s[n-1]);
    return 0;
}
